class Producto:

    def __init__(self,id,nombre,cantidad,precio):

        self.id = id
        self.nombre = nombre
        self.cantidad = cantidad
        self.precio = precio


class Cliente:

    def __init__(self,id,nombre,correo,telefono,direccion):

        self.id = id
        self.nombre = nombre
        self.correo = correo
        self.telefono = telefono
        self.direccion = direccion
